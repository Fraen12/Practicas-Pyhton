#Operadores Aritméticos
print(2 + 2)
print(7 - 4)
print(3 * 5)
print(8 / 2)
print(11 % 4) # Operador Módulo (Residuo en una división)
print(11 // 4) # Operador de Piso (Cantídad de veces que cabe un número)
print(2 ** 3) #Operador Potencia

#Operadores en Cadenas de texto(Strings)
print("Hola" + "Mundo") # Suma de strings = "Concatenación"
print("Hola" * 3) # Multiplicación de strings = "Repetición"
print("hola" + "Mundo" * 3)

#Operadores de Comparación(Booleano)
print("Hola" == "hola") # Igual qué [Falso o True{Comparación booleana}]
print("hola" != "hola") # Distinto qué [Falso o True{Comparación booleana}]
print(3 > 5) # Mayor qué Menor qué (> ó <)
print(5 >= 3) #Mayor o Igual qué / Menor o igual qué (>= ó <=)

#Operadores de Existencia
print("ola" in "Hola") # "Existencia" (Si una cadena forma parte de otra cadena)
print("ola" not in "Hola") # "Inexistencia" (Si una cadena NO forma parte de otra cadena)

#Operadores Booleanos
print(True or False) # or Verifica que se cumpla sólo una condición
print(True and False) # and Verifica que se cumplan las dos condiciones

#Operadores de Asignación
x = 2 # Operador de asignación estándar
x += 2 # Operador de asignación suma o resta
x *= 3 # Operador de asignación multiplicación
x %= 4 #Operador de asignación módulo
print(x)

Y = 6 * 5 + 8 / 2 ** 4
print(Y)