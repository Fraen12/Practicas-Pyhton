#Varrable Entero
numero = 147
print(numero)
print(type(numero))
#Variable con punto decimal
numero = 147.5
print(numero)
print(type(numero))
#Numero complejos
complejo = 1 + 2j
print(complejo)
print(type(complejo))
#Cadenas de texto
palabra = "Hola"
print(palabra)
print(type(palabra))
#Variables de tipo lógico(Lógica Booleana)
bandera = True
print(bandera)
print(type(bandera))
#Valor númerico nulo
vacio = None
print(vacio)
print(type(vacio))
